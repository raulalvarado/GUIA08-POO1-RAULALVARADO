package com.sv.udb.modelo;

/**
 *
 * @author Raul
 */
public class Jugadores {
    private int id;
    private Equipos equipo;
    private String nombre;
    private int edad;
    private double altura;
    private double peso;

    public Jugadores() {
    }

    public Jugadores(int id, Equipos equipo, String nombre, int edad, double altura, double peso) {
        this.id = id;
        this.equipo = equipo;
        this.nombre = nombre;
        this.edad = edad;
        this.altura = altura;
        this.peso = peso;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Equipos getEquipo() {
        return equipo;
    }

    public void setEquipo(Equipos equipo) {
        this.equipo = equipo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    @Override
    public String toString() {
        return nombre;
    }
    
    
    
}


